    <p>
      Deciding to hire an online Islamic tutor is a different kind of commitment from signing up for a course. A course is a packaged product. A tutor is a person you'll work with weekly for months or years. This article describes what the first month with an online Islamic tutor actually looks like so you can tell whether what you're experiencing is normal and on track.
    </p>
    <p>
      The shape of lessons will vary by subject. Quran study is close, corrective teaching where the teacher listens to every recitation. Arabic runs similarly. Fiqh is usually taught in a small group (it suits the material). The notes below apply across all three, with differences called out where they matter.
    </p>

    <h2>Before the first lesson: the conversation</h2>
    <p>
      A serious tutor, or a serious programme, will want to have a real conversation with you before the first lesson. It's not a sales call. The goal is to understand what you want, what your starting point is, and whether there's a plausible plan that fits. The conversation usually covers:
    </p>
    <ul>
      <li><strong>Goals.</strong> Are you starting Quran from scratch? Polishing tajwid? Interested in Arabic for Quran or Arabic for professional fluency? Fiqh from zero, or with prior study?</li>
      <li><strong>Current level.</strong> What have you studied before? What can you read? What do you struggle with?</li>
      <li><strong>Timing.</strong> How many lessons per week can you realistically commit to? What part of your week is most protected from interruption?</li>
      <li><strong>Context.</strong> Are you studying for yourself, for a child, or planning family lessons? Any specific events (Ramadan, an upcoming trip) that will affect the early months?</li>
    </ul>
    <p>
      A tutor who skips this conversation, or turns it into a pure pricing discussion, is signalling how they approach the relationship. A tutor who asks careful, substantive questions is already teaching.
    </p>

    <h2>The assessment</h2>
    <p>
      The first proper session is usually an assessment rather than a lesson. It looks different depending on what you're studying.
    </p>
    <h3>For Quran students</h3>
    <p>
      You read a passage of the tutor's choice. Short if you're a beginner, longer and more varied if you're more advanced. They listen, take notes, and form a view on your articulation, your tajwid, your memorisation if relevant, and the consistency of all of the above. They may ask you to recite something specific to test particular rules. Expect about thirty to forty-five minutes of mostly you reading, with the tutor interjecting corrections or questions.
    </p>
    <h3>For Arabic students</h3>
    <p>
      A short reading test and a conversation about what you already know grammatically. The tutor may show you a few example sentences and ask what role each word plays, or walk you through a small passage and ask for translation. If you've studied from a textbook, they'll ask which one and where you left off. The point is to calibrate: to find the edge of what you do know, not to test what you don't.
    </p>
    <h3>For fiqh students</h3>
    <p>
      Usually a conversation rather than a reading test. The tutor asks what you've studied before, what topics interest you most, what your practice context is (which madhhab do you follow, what questions are you encountering). They may describe the sequence of manuals they typically teach and ask where you think you'd fit.
    </p>
    <p>
      At the end of the assessment, a good tutor tells you clearly: here's where I think you are, here's what I'd propose for the next three months, and here's what that looks like in a typical lesson. If none of that is forthcoming, ask directly.
    </p>

    <h2>The first three lessons</h2>
    <p>
      The first few real lessons tend to feel a little cautious on both sides. The tutor is still calibrating to your pace. You're still adjusting to the rhythm of live online study. What should be clear by the end of lesson three:
    </p>
    <ul>
      <li>A specific curriculum is underway. You know what you read last lesson and what you'll read next lesson.</li>
      <li>The tutor remembers things from previous lessons. Not just the passage but the mistake, the thing you asked, the part you struggled with.</li>
      <li>Corrections are specific, not general. Not "try to be more careful" but "hold the ra firmer, articulation is closer to the gum than the teeth".</li>
      <li>In Quran and Arabic, you're reading more than you're being lectured at. The lesson time is yours, not a delivery vehicle for the tutor's knowledge. In fiqh, the balance is different: the teacher reads the manual, explains, and you ask questions.</li>
    </ul>
    <p>
      If those aren't happening by lesson three, it's worth naming it directly. A good tutor welcomes the feedback and adjusts. A less good one gets defensive. Either way, you learn something useful.
    </p>

    <h2>The first month: settling into rhythm</h2>
    <p>
      By lesson four or five, you'll be in a rhythm. The specific shape depends on the subject, but the pattern is similar: each lesson has a focus, you prepare between lessons, the tutor sees what the preparation produced and adjusts.
    </p>
    <p>
      What "making progress" looks like is different from what students often expect:
    </p>
    <ul>
      <li>Not dramatic weekly breakthroughs.</li>
      <li>Small, cumulative corrections that gradually stick.</li>
      <li>The sense that the tutor is watching the trajectory, not just the current session.</li>
      <li>Periodically, a moment (usually after a month or two) where something that was hard starts feeling natural.</li>
    </ul>
    <p>
      A common early-month mistake is to judge progress by how much you covered. The better measure is whether the things you covered last week are still correct this week. Coverage without retention is motion, not progress.
    </p>

    <h2>Red flags in the first month</h2>
    <p>
      Things that, if you see them, warrant a direct conversation with the tutor or a decision about whether to continue:
    </p>
    <ul>
      <li><strong>No curriculum visible.</strong> Each lesson is "whatever you want to read today". Fine for casual review; not what paid tutoring should look like.</li>
      <li><strong>Corrections don't stick.</strong> The same mistake is corrected in every lesson and never seems to reduce.</li>
      <li><strong>Unreliability.</strong> Cancellations, late starts, sessions cut short. Everyone has a bad week; a pattern is a pattern.</li>
      <li><strong>Lectures instead of lessons (in Quran and Arabic).</strong> The tutor talks for most of the hour. Even good knowledge delivered at you rather than drawn out of you is expensive listening for those subjects.</li>
      <li><strong>No notes, no memory between sessions.</strong> You come back from holiday and the tutor doesn't remember where you were.</li>
    </ul>
    <p>
      One or two of those in an early month is normal; everyone's still calibrating. A pattern across the first month is the signal to either raise it directly or look elsewhere.
    </p>

    <h2>Practical logistics</h2>
    <p>
      A few things worth getting clear early so they don't become tensions later:
    </p>
    <ul>
      <li><strong>Makeup lessons.</strong> If you miss, is it lost? If the tutor misses, what happens?</li>
      <li><strong>Payment rhythm.</strong> Is it monthly, per-session, or in blocks? When is it due?</li>
      <li><strong>Communication between lessons.</strong> Is it okay to send a quick question on WhatsApp, or is all communication reserved for the session?</li>
      <li><strong>Holidays.</strong> How does Ramadan work? Eid? Tutor's own holidays?</li>
    </ul>
    <p>
      None of these have universally correct answers. They depend on the tutor and your preferences. What matters is agreeing the expectations early so there's no ambiguity six months in.
    </p>

