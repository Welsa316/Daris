    <p>
      Deciding to hire a private Islamic tutor online is a different kind of commitment from signing up for a course. A course is a packaged product; a tutor is a person you will work with weekly for months or years. This article describes what the first month with a private online Islamic tutor actually looks like — the goal-setting, the assessment, the first lessons — so you can tell whether what you are experiencing is normal and on track.
    </p>

    <h2>Before the first lesson: the conversation</h2>
    <p>
      A serious private tutor, or a serious programme with a single-tutor model, will want to have a real conversation with you before the first lesson. This is not the same as a sales call. The goal is to understand what you want, what your starting point is, and whether there is a plausible programme that fits. In practice the conversation covers:
    </p>
    <ul>
      <li><strong>Goals.</strong> Are you starting Quran from scratch? Polishing tajwid? Planning toward ijazah? Interested in Arabic for Quran or Arabic for professional fluency? Fiqh from zero, or with some prior study?</li>
      <li><strong>Current level.</strong> What have you studied before? What can you read? What do you struggle with?</li>
      <li><strong>Timing.</strong> How many lessons per week can you realistically commit to? What part of your week is most protected from interruption?</li>
      <li><strong>Context.</strong> Are you studying for yourself, for a child, or planning on family lessons? Do you have specific events (Ramadan, an upcoming trip) that will affect the early months?</li>
    </ul>
    <p>
      A tutor who skips this conversation — or turns it into a pure pricing discussion — is signalling how they approach the relationship. A tutor who asks careful, substantive questions is already teaching.
    </p>

    <h2>The assessment</h2>
    <p>
      The first proper session is usually an assessment rather than a lesson. It looks different depending on what you are studying:
    </p>
    <h3>For Quran students</h3>
    <p>
      You read a passage of the tutor's choice — short if you are a beginner, longer and more varied if you are more advanced. They listen, take notes, and form a view on your articulation, your tajwid, your memorisation if relevant, and the consistency of all of the above. They may ask you to recite something specific to test particular rules. Expect about thirty to forty-five minutes of mostly you reading, with the tutor interjecting corrections or questions.
    </p>
    <h3>For Arabic students</h3>
    <p>
      A short reading test and a conversation about what you already know grammatically. The tutor may show you a few example sentences and ask what role each word plays, or walk you through a small passage and ask for translation. If you have studied from a textbook, they will ask which one and where you left off. The point is to calibrate — not to test what you do not know but to find the edge of what you do.
    </p>
    <h3>For fiqh students</h3>
    <p>
      Usually a conversation rather than a reading test. The tutor asks what you have studied before, what topics interest you most, what your practice context is (what madhhab do you follow, what questions are you encountering in your own life). They may describe the sequence of manuals they typically teach and ask where you think you would fit in that sequence.
    </p>
    <p>
      At the end of the assessment, a good tutor tells you clearly: here is where I think you are, here is what I would propose we do for the next three months, and here is what that would look like in a typical lesson. If none of that is forthcoming, ask directly.
    </p>

    <h2>The first three lessons</h2>
    <p>
      The first few real lessons tend to feel a little cautious on both sides. The tutor is still calibrating to your pace; you are still adjusting to the rhythm of live online study. What should be clear by the end of lesson three:
    </p>
    <ul>
      <li>A specific curriculum is underway. You know what you read last lesson and what you will read next lesson.</li>
      <li>The tutor remembers things from previous lessons — not just the passage but the mistake, the thing you asked, the part you struggled with.</li>
      <li>Corrections are specific, not general. Not "try to be more careful" but "hold the ra firmer, the articulation is closer to the gum than the teeth".</li>
      <li>You are reading more than you are being lectured at. The lesson time is yours, not a delivery vehicle for the tutor's knowledge.</li>
    </ul>
    <p>
      If any of those are missing by lesson three, it is worth naming it directly. A good tutor welcomes the feedback and adjusts. A less good one gets defensive. Either way, you learn something useful.
    </p>

    <h2>The first month: settling into rhythm</h2>
    <p>
      By lesson four or five, you will be in a rhythm. The specific shape depends on the subject, but the pattern is similar: each lesson has a focus, you prepare between lessons, the tutor sees what the preparation produced and adjusts. What "making progress" looks like is different from what students often expect:
    </p>
    <ul>
      <li>Not dramatic weekly breakthroughs.</li>
      <li>Small, cumulative corrections that gradually stick.</li>
      <li>The sense that the tutor is watching the trajectory, not just the current session.</li>
      <li>Periodically, a moment — usually after a month or two — where something that was hard starts feeling natural.</li>
    </ul>
    <p>
      A common early-month mistake is to judge progress by how much you covered. The better measure is whether the things you covered last week are still correct this week. Coverage without retention is motion, not progress.
    </p>

    <h2>Red flags in the first month</h2>
    <p>
      Things that, if you see them, warrant a direct conversation with the tutor or a decision about whether to continue:
    </p>
    <ul>
      <li><strong>No curriculum visible.</strong> Each lesson is "whatever you want to read today". Fine for casual review; not what paid tutoring should look like.</li>
      <li><strong>Corrections do not stick.</strong> The same mistake is corrected in every lesson and never seems to reduce.</li>
      <li><strong>Unreliability.</strong> Cancellations, late starts, sessions cut short. Everyone has a bad week; a pattern is a pattern.</li>
      <li><strong>Lectures instead of lessons.</strong> The tutor talks for most of the hour. Even good knowledge delivered at you rather than drawn out of you is expensive listening.</li>
      <li><strong>No notes, no memory between sessions.</strong> You come back from holiday and the tutor does not remember where you were.</li>
    </ul>
    <p>
      One or two of these in an early month is normal — everyone is still calibrating. A pattern across the first month is the signal to either raise it directly or look elsewhere.
    </p>

    <h2>Practical logistics</h2>
    <p>
      A few things worth getting clear in the first month so they do not become tensions later:
    </p>
    <ul>
      <li><strong>Makeup lessons.</strong> If you miss, is it lost? If the tutor misses, what happens?</li>
      <li><strong>Payment rhythm.</strong> Is it monthly, per-session, or in blocks? When is it due?</li>
      <li><strong>Communication between lessons.</strong> Is it okay to send a quick question on WhatsApp, or is all communication reserved for the session?</li>
      <li><strong>Holidays.</strong> How does Ramadan work? Eid? Tutor's own holidays?</li>
    </ul>
    <p>
      None of these have universally correct answers — they depend on the tutor and your preferences. What matters is agreeing the expectations early so there is no ambiguity six months in.
    </p>

