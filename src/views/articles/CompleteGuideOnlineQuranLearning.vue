    <h2>What "online Quran" actually means</h2>
    <p>
      At least three different things get labelled the same way:
    </p>
    <ul>
      <li><strong>Apps and courseware.</strong> Self-paced drills, flashcards, reader apps. Useful as a supplement, almost useless as a primary path because nobody is listening to you read.</li>
      <li><strong>Recorded courses.</strong> Pre-produced videos on YouTube or paid platforms. Better for theory, still no substitute for live correction.</li>
      <li><strong>Live tuition.</strong> A qualified teacher over video call hears you recite and corrects what you actually said, not what the recording imagines you would say.</li>
    </ul>
    <p>
      The third category is the one that actually teaches Quran, because tajwid is physical. It's about how your tongue sits against your teeth, how your lips shape a letter, whether the sound emerges from the throat or the palate. You can't hear most of your own mistakes. A trained ear can hear them on the first try.
    </p>

    <h2>What makes a good programme</h2>
    <h3>Live correction every week</h3>
    <p>
      Every session should include the student reading aloud and the teacher correcting what came out wrong. If most of the hour is explanation, the dosage of corrective listening is too low.
    </p>
    <h3>A documented sequence</h3>
    <p>
      Articulation of the letters (makhārij al-ḥurūf) before tajwid rules. Tajwid rules before serious memorisation. The classical order exists because each step relies on the one before it. A programme that skips ahead is setting up a cleanup job later.
    </p>
    <h3>A qualified teacher</h3>
    <p>
      "Qualified" has a specific meaning in Quranic study. The teacher themselves studied with a teacher, whose teacher studied with a teacher, going back through the generations. The best teachers can point to their lineage in plain terms. Not everyone has a specific ijazah in a specific reading, but a serious teacher can tell you who they learned from and where.
    </p>
    <h3>Frequency that actually sticks</h3>
    <p>
      Two one-hour lessons a week is the floor for consistent progress. Three is where most serious students settle. One weekly lesson is a slow track. That's fine if it's an honest choice, less fine if you're paying for three and getting the progress of one.
    </p>

    <h2>What the arc looks like</h2>
    <h3>Absolute beginner</h3>
    <p>
      You don't know the Arabic letters, or you can identify them but can't pronounce them correctly. The first months are letter articulation, one letter at a time, and short simple verses to practise the shapes you have learned. After two or three months you're reading short surahs slowly, with the teacher correcting every pass. This stage is frustrating by design. Rushing it is how bad habits embed.
    </p>
    <h3>Intermediate</h3>
    <p>
      You can sound out Quranic Arabic but your recitation doesn't follow tajwid. The programme works through the rules in sequence, applying each to short passages and building up. Six to twelve months of steady work usually gets a motivated student to reliable applied tajwid across most of the Quran.
    </p>
    <h3>Memorisation (ḥifẓ)</h3>
    <p>
      Memorisation is its own discipline. A student doing one page a day, five days a week, with disciplined revision, can finish the Quran in two to three years. Memorisation without tajwid first is harmful: you lock in incorrect recitation and memorise the incorrect version. Any programme that puts memorisation before articulation is skipping a step you will pay for later.
    </p>
    <h3>Advanced</h3>
    <p>
      The advanced student recites accurately and is working on the level required for ijazah in a specific reading (most commonly Ḥafṣ ʿan ʿĀṣim). That means reciting the whole Quran to the teacher multiple times with minimal correction, often alongside the written tajwid treatises that formalise the rules. Ijazah is a real qualification in a real lineage. It isn't issued lightly, and it isn't issued quickly. Plan in years, not months.
    </p>

    <h2>What to ask before you enrol</h2>
    <p>
      Most prospective students ask the wrong questions first (price, schedule, format) and never get to the ones that matter. Better list:
    </p>
    <ul>
      <li>Who trained the teacher? In what tradition?</li>
      <li>What's the curriculum sequence, step by step, for someone at my level?</li>
      <li>Do I work with a single teacher over time, or do I get rotated?</li>
      <li>How is revision built in? What happens when I miss a lesson?</li>
      <li>Can I sample a lesson before committing?</li>
      <li>What do students at my starting point actually achieve after six months? After a year?</li>
    </ul>
    <p>
      Good programmes answer these directly. Less serious ones pivot to pricing and testimonials. Testimonials are useful. Curriculum answers are more useful.
    </p>

    <h2>What goes wrong</h2>
    <p>
      The common failure modes, in rough order:
    </p>
    <ul>
      <li><strong>Skipping articulation.</strong> Student "just wants to read surahs". Two years later their ra is consistently wrong and will need six months of unlearning.</li>
      <li><strong>Under-commitment.</strong> One weekly lesson with no revision between. Progress plateaus. The student blames the programme. The programme is fine, the dosage is too low.</li>
      <li><strong>Rotating teachers.</strong> Different teacher each month "for variety". Nobody builds a longitudinal view. Corrections that should have been caught get missed because every teacher sees week one of their own view.</li>
      <li><strong>Memorisation before tajwid.</strong> Student sounds impressive for a year. Year two the errors surface and can't be undone without restarting.</li>
    </ul>
    <p>
      None of those are failures of "online". They are failures of a specific programme. A well-structured online programme avoids all four. A badly structured in-person programme runs into the same four.
    </p>

