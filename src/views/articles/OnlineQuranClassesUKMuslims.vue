    <h2>Timezones: GMT vs the Arab world</h2>
    <p>
      The UK sits on GMT in winter and BST (GMT+1) in summer. Most Quran teachers you will find online are based somewhere in the Arab world — typically GMT+2 (Egypt) or GMT+3 (the Gulf states). Practical implications:
    </p>
    <ul>
      <li>A UK afternoon (e.g. 5–6pm UK time) is evening for the teacher (7–9pm their time) — mutually comfortable.</li>
