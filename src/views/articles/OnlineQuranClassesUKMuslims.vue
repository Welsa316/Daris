      <li>A UK morning (before 10am) is afternoon for the teacher — usually easy to schedule.</li>
      <li>The clock-change weeks in March and October can shift your slot by an hour on their side; agree in advance whether the slot is anchored to UK time or to local time.</li>
    </ul>
    <p>
      For a child studying after school, the practical window is usually 4–7pm UK time. For a working adult studying before or after work, the practical window is 6–9am or 6–10pm UK time. Both work; confirm with the teacher up front.
    </p>

    <h2>Frequency and the UK working week</h2>
