    <h2>Timezones: GMT and the Arab world</h2>
    <p>
      The UK sits on GMT in winter and BST (GMT+1) in summer. Most Quran teachers you'll find online are based somewhere in the Arab world, typically GMT+2 (Egypt) or GMT+3 (the Gulf). Practical implications:
    </p>
