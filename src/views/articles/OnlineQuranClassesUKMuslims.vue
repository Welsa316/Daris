    <h2>What to look for in a teacher if you are in the UK</h2>
    <p>
      Most of what makes a teacher a good fit is universal — lineage, curriculum, correction style, lesson format. Some things are UK-specific:
    </p>
    <ul>
      <li><strong>Working English</strong>. Even if instruction happens primarily in Arabic for fluent Arabic speakers, UK students — especially children and revert Muslims — benefit from a teacher who can explain in English where needed.</li>
      <li><strong>Understanding of the UK context</strong>. A teacher who understands why your child cannot have a lesson during school hours, or that Friday evening is family time, will make fewer suggestions that do not fit.</li>
      <li><strong>Reliability across the time difference</strong>. Check that the teacher treats the UK slot with the same reliability they would treat a local slot.</li>
    </ul>
    <p>
      Ask, during the trial, how other UK students structure their week with the teacher. If the teacher has experience with UK schedules, they will have examples ready.
