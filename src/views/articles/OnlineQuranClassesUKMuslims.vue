      <li>A UK morning (before 10am) is afternoon for the teacher. Usually easy to schedule.</li>
      <li>The clock-change weeks in March and October can shift your slot by an hour on their side. Agree in advance whether the slot is anchored to UK time or to local time.</li>
    </ul>
