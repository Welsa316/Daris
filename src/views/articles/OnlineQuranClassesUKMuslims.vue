      <li>Reduce frequency during Ramadan if needed. A serious programme accommodates this rather than dropping students for skipped weeks.</li>
      <li>Some students prefer to take a planned Ramadan pause and return afterwards. Communicate the decision in advance so the teacher holds the slot.</li>
      <li>Ramadan-specific study topics (tafsir of Juz ʿAmma, revision of memorised portions for Taraweeh) can replace regular curriculum during Ramadan and resume afterwards.</li>
    </ul>
    <p>
      UK school holidays (Easter, summer, October half-term, Christmas) are worth planning around too. Children's programmes generally pause for summer and resume in September. Adult programmes usually continue with occasional agreed breaks.
