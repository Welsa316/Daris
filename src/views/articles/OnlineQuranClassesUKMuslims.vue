      <li><strong>Working English.</strong> Even if instruction happens primarily in Arabic for fluent Arabic speakers, UK students (especially children and revert Muslims) benefit from a teacher who can explain in English where needed.</li>
      <li><strong>Understanding of the UK context.</strong> A teacher who understands why your child can't have a lesson during school hours, or that Friday evening is family time, will make fewer suggestions that don't fit.</li>
      <li><strong>Reliability across the time difference.</strong> Check that the teacher treats the UK slot with the same reliability they would treat a local one.</li>
    </ul>
