    <h2>Balancing lessons with family life</h2>
    <p>
      The single most common reason UK students drop out of online Quran programmes is not quality. It's that they over-committed at the start and couldn't sustain it alongside work and family.
    </p>
    <p>
      If you're starting fresh, err on the side of two lessons a week, not three. You can always increase frequency once you have a rhythm. Shrinking back from three to two feels like failure even when it's the sensible call. Two reliable lessons a week for two years produces more than three unreliable lessons a week for six months.
