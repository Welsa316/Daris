    <p>
      Most "online Quran academy" listings blur together. Same stock photos, same "qualified teachers from Al-Azhar", same vague promises about tajwid and ijazah. After enough trial lessons it becomes clear that price and website design are almost useless as filters. Here are seven specific checks that actually tell you whether a programme is serious.
    </p>

    <h2>1. Ask who trained the teacher</h2>
    <p>
      The single most informative question is: "Who taught you?" A qualified Quran teacher can answer it without hesitation. They'll name their own teacher, that teacher's background, and (often) the chain further back.
    </p>
    <p>
      If the answer is a generic "I studied at Al-Azhar" with no specific teacher named, you're looking at institutional affiliation without a personal chain. That's a yellow flag. If the answer is "I learned on my own" or "from YouTube", thank them and leave.
    </p>
    <p>
      Quranic transmission has always been person to person. The classical measure of a teacher's authority is the lineage they can point to.
    </p>

    <h2>2. Ask them to walk through the curriculum</h2>
    <p>
      Any serious programme has a documented sequence. "We start with letter articulation, then noon sakin and tanwin, then madd, then qalqala…" should roll out fluently.
    </p>
    <p>
      If the teacher can't articulate the sequence without stumbling, or describes it as "we adapt to the student" without a skeleton underneath, the adaptation is cover for lack of structure. Adapting a real curriculum is fine. Having no curriculum is not adaptation.
    </p>
    <p>
      Good test: "What would my first six months look like?" A prepared teacher gives a concrete answer.
    </p>

    <h2>3. Ask what they do when a student makes a specific mistake</h2>
    <p>
      Concrete test: "Suppose I'm reading and I say the ح like a خ. What do you do?" A good teacher describes a specific correction procedure: demonstrating the correct articulation, having the student repeat, comparing the two, drilling.
    </p>
    <p>
      A weaker teacher answers in generalities ("I correct them") or jumps to the rule rather than the physical articulation. You're trying to find out whether this person actually teaches, not whether they can describe teaching.
    </p>

    <h2>4. Ask about frequency of correction</h2>
    <p>
      "What's the ratio of student-reading time to teacher-speaking time in a typical lesson?" The right answer is heavily skewed toward the student reading, because the lesson's purpose is the teacher hearing you, not the teacher explaining.
    </p>
    <p>
      Teachers who talk a lot during the lesson are often knowledgeable but aren't using the lesson time well. A fifty-minute lesson where the teacher delivered a twenty-minute explanation is twenty minutes of wasted tuition.
    </p>

    <h2>5. Ask to sample a lesson, and pay attention to what happens</h2>
    <p>
      Almost every programme offers a trial. Use it as a real evaluation, not a formality. Prepare a short passage. Read it slowly. Pay attention to whether the teacher:
    </p>
    <ul>
      <li>Catches errors you know you made.</li>
      <li>Corrects the articulation specifically, or just restates the rule.</li>
      <li>Has you repeat until the correction sticks, or moves on after one attempt.</li>
      <li>Takes notes on what went wrong, or relies on memory.</li>
      <li>Starts next week by referencing what happened this week.</li>
    </ul>
    <p>
      One trial lesson tells you a lot. Two trial lessons with the same teacher tell you almost everything you need.
    </p>

    <h2>6. Ask about the format that fits the subject</h2>
    <p>
      For Quran specifically, each student needs the teacher's ear individually. A teacher can't listen to four recitations at once. That's why corrective Quran teaching is close and individual.
    </p>
    <p>
      Group formats are fine for theory (tajwid rules in the abstract, Arabic grammar concepts, tafsir lectures, fiqh texts read aloud together) but not for the part of Quran study that requires the teacher to hear you read. Ask the programme how it handles the difference. A serious one has a clear answer.
    </p>

    <h2>7. Ask what happens when you miss lessons</h2>
    <p>
      Everyone misses lessons. Work travel, illness, Ramadan scheduling, life. What matters is what the programme does about it:
    </p>
    <ul>
      <li>Is there a formal makeup policy, or do missed lessons vanish?</li>
      <li>When you come back after missing, does the teacher remember where you were?</li>
      <li>Is there a revision rhythm built into the curriculum, or does every lesson assume perfect retention from last week?</li>
    </ul>
    <p>
      A serious programme accounts for gaps because gaps are inevitable. A loose programme treats every lesson as independent and relies on the student to pick up where they left off. Over a year, that difference compounds.
    </p>

    <h2>Bonus question: what do they say when you ask about ijazah?</h2>
    <p>
      If the answer is "everyone who finishes our programme gets one", be cautious. Ijazah is earned by demonstrated capability, not by programme completion. If the answer is "let's see what you can do first", you're talking to someone who takes it seriously. See our <a href="/en/articles/what-is-ijazah-and-how-to-earn-it-online">separate article on ijazah</a> for the full picture.
    </p>

    <h2>What to do if a programme fails these checks</h2>
    <p>
      Walk away. There are more online Quran programmes than there were a decade ago. It isn't a scarce market. The cost of staying in the wrong one for a year is leaving that year with bad habits that then take another year to unlearn. The cost of leaving is a week of searching for a better fit.
    </p>
