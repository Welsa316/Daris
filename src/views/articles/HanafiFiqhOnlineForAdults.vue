    <h2>Why the Hanafi school specifically</h2>
    <p>
      The Hanafi school is the oldest of the four surviving Sunni schools of jurisprudence, traced to Abū Ḥanīfa (d. 150 AH) and developed by his leading students Abū Yūsuf and Muḥammad al-Shaybānī. It became the legal school of the Abbasid caliphate and later the Ottoman Empire. Today it is the dominant school across the Indian subcontinent, Turkey, parts of the Arab world, and significant Muslim communities in the United Kingdom and elsewhere.
    </p>
    <p>
      Choosing a school is not about picking the "right" one versus three "wrong" ones. All four Sunni schools are valid expressions of Islamic jurisprudence. Following one — whichever one fits your community, your teachers, or your family tradition — is how the scholarly tradition has historically worked. What matters is taking your chosen school seriously: studying its manuals with qualified teachers rather than assembling opinions from fragmented online sources.
    </p>

    <h2>The classical manuals</h2>
    <p>
      Hanafi fiqh has a well-established body of manuals used across centuries for systematic instruction. The major ones students are likely to encounter:
    </p>
    <ul>
      <li><em>Nūr al-Īḍāḥ</em> by al-Shurunbulālī — a short, accessible primer on worship (<em>ʿibādāt</em>). Common starting point for beginners.</li>
      <li><em>Marāqī al-Falāḥ</em> — the author's own longer commentary on <em>Nūr al-Īḍāḥ</em>, extending the coverage.</li>
      <li><em>Al-Hidāya</em> by al-Marghīnānī — the definitive medieval Hanafi manual, covering worship, transactions, and a full legal curriculum. Substantial; typically reserved for advanced students.</li>
      <li><em>Mukhtaṣar al-Qudūrī</em> — another concise manual widely used as a teaching text.</li>
    </ul>
    <p>
      A good programme does not hand you <em>al-Hidāya</em> in month one. It starts with a short primer — often <em>Nūr al-Īḍāḥ</em> — and progresses through increasingly detailed texts as your grounding deepens. The point of the manuals is to transmit not just rulings but the reasoning behind them; reading them with a teacher who can explain the classical legal reasoning is different from reading them alone.
    </p>

    <h2>What a serious programme covers</h2>
    <p>
      Fiqh for adults in a structured Hanafi programme typically covers:
    </p>
    <h3>ʿIbādāt — the acts of worship</h3>
    <p>
      Purity (<em>ṭahāra</em>), prayer (<em>ṣalāt</em>), fasting (<em>ṣawm</em>), zakat, hajj. This is the core of most foundational fiqh instruction. It is where most everyday questions arise — the validity of ablution, the rules of the five daily prayers, rulings specific to women's fiqh, common issues in travel and illness. A good programme walks through each topic section by section with the manual open, explains what the ruling is and the reasoning behind it, and answers questions the student brings from their own practice.
    </p>
    <h3>Muʿāmalāt — transactions and contracts</h3>
    <p>
      The Hanafi tradition has an extensive body of jurisprudence on contracts, trade, partnerships, loans, inheritance, and so on. Students in contemporary contexts often come to muʿāmalāt with questions about Islamic finance, halal business practice, contemporary contracts. A serious programme introduces the classical framework first — the structure of the classical contracts, the principles of Islamic commercial law — before engaging with contemporary questions. Skipping the framework to go directly to "is X halal" produces brittle knowledge that cannot handle variations.
    </p>
    <h3>Personal and family fiqh</h3>
    <p>
      Marriage (<em>nikāḥ</em>), divorce, custody, inheritance. These topics carry real-life stakes and are often where students most want expert guidance. Like muʿāmalāt, they should be studied within the classical framework before contemporary questions are addressed.
    </p>

    <h2>Aqeedah alongside fiqh</h2>
    <p>
      Traditional Hanafi instruction has always included <em>aqeedah</em> (Islamic creed) alongside fiqh, typically using classical primers like <em>al-ʿAqīda al-Ṭaḥāwiyya</em> or shorter works adapted for beginners. A fiqh programme that leaves aqeedah entirely aside is incomplete. Most students study aqeedah in shorter, more focused blocks interspersed with the longer fiqh sequence — a few months of aqeedah every year or two alongside continuing fiqh study.
    </p>

    <h2>Hadith and mustalah al-hadith</h2>
    <p>
      At a certain point, advanced students benefit from engagement with hadith in the original and with <em>mustalah al-ḥadīth</em> — the science of hadith classification and authentication. This is not beginner material; it typically comes after the student has a grounding in fiqh, Arabic, and the basics of how classical scholarship uses hadith. For serious adult learners pursuing a full curriculum, it is worth planning for as a multi-year horizon.
    </p>

    <h2>How online delivery actually works</h2>
    <p>
      Fiqh study translates well to the online format — better, in some ways, than Quranic recitation, because the live correction of pronunciation is less central. What a good online fiqh lesson looks like:
    </p>
    <ul>
      <li>The student has the manual open (physical or digital) at the agreed passage.</li>
      <li>The teacher reads the passage aloud in Arabic, with the student following along.</li>
      <li>The teacher explains the rulings section by section, the reasoning behind them, and the traditional commentary.</li>
      <li>The student asks questions — about the classical material, about how it applies to their own life, about where opinions vary.</li>
      <li>The session ends with a clear agreement on what the student will read before the next lesson.</li>
    </ul>
    <p>
      This is a normal and legitimate form of transmission. Classical fiqh instruction has always involved sitting with a teacher and reading a manual together; the camera does not change the substance.
    </p>

    <h2>Language of instruction</h2>
    <p>
      Traditional fiqh manuals are written in Arabic, and serious instruction happens in Arabic — the vocabulary of Hanafi fiqh has classical Arabic terms that English translations approximate rather than replace. For adult students whose Arabic is still developing, this creates a real question about which to do first: Arabic foundations, then fiqh in Arabic, or fiqh with English support until Arabic catches up.
    </p>
    <p>
      The honest answer is that both work, with trade-offs. Starting fiqh with English support lets you make progress on the content earlier; you will re-read the manuals in Arabic later, and that second pass will be deeper. Starting with Arabic first defers the fiqh but produces more durable fiqh study when you begin. Many programmes allow bilingual lessons for exactly this reason — instruction primarily in Arabic, with English explanation where the Arabic gets too dense for the student's current level.
    </p>

