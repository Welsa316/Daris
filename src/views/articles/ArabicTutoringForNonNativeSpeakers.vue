    <p>
      There are more Arabic-learning tools now than there were a decade ago. Apps, subscription platforms, YouTube channels, PDFs, Anki decks, AI conversation partners, university MOOCs. There are also more stalled Arabic students. This article sorts the tools by what they're actually good at, and describes what a serious progression looks like for a non-native speaker whose goal is Classical Arabic.
